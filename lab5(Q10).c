#include <stdio.h>
int main(){
	int num=12;
	
	printf("left shift by two of number twelve is: %d\n" , num<<2);
	printf("left shift by one of number twelve is: %d\n" , num<<1);
	
	printf("right shift by two of number twelve is: %d\n" , num>>2);
	printf("right shift by one of number twelve is: %d\n" , num>>1);
}
